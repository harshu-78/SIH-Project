/* ---------- Voice assistant (speak page + optional switch) ---------- */
const voiceBtn = document.getElementById('voiceBtn');
let synth = window.speechSynthesis;
function speakCurrent(lang) {
  const spoken = [
    document.querySelector('[data-translate="hero_title"]')?.textContent || '',
    document.querySelector('[data-translate="hero_sub"]')?.textContent || '',
    document.querySelector('[data-translate="hero_desc"]')?.textContent || ''
  ].join('. ');
  if (!synth) { alert('Speech synthesis not supported in this browser (demo).'); return; }
  const utter = new SpeechSynthesisUtterance(spoken);
  // choose voice by lang if available
  utter.lang = lang || (localStorage.getItem('sv_lang') || 'en');
  synth.cancel();
  synth.speak(utter);
}

/* optional voice command recognition to switch language (if supported) */
let recognition = null;
if ('webkitSpeechRecognition' in window || 'SpeechRecognition' in window) {
  const SR = window.SpeechRecognition || window.webkitSpeechRecognition;
  recognition = new SR();
  recognition.lang = 'en-US';
  recognition.interimResults = false;
  recognition.maxAlternatives = 1;
  recognition.onresult = (ev) => {
    const text = ev.results[0][0].transcript.toLowerCase();
    // simple phrases: "malayalam", "change to hindi", etc.
    const mapping = { malayalam:'ml', malayalum:'ml', hindi:'hi', english:'en', bengali:'bn', odia:'or', odia_alt:'ori', tamil:'ta', kannada:'kn', kannÄá¸a:'kn' };
    for (const k in mapping) {
      if (text.includes(k)) {
        const code = mapping[k];
        if (TRANSLATIONS[code]) {
          document.getElementById('languageSelect').value = code;
          if (document.getElementById('languageSelectMobile')) document.getElementById('languageSelectMobile').value = code;
          translatePage(code);
          alert('Language switched to: ' + code);
          return;
        }
      }
    }
    // otherwise, just speak the transcript
    speakCurrent(localStorage.getItem('sv_lang') || 'en');
  };
  recognition.onerror = (e) => { console.log('rec error', e); };
}

voiceBtn.addEventListener('click', ()=> {
  // speak current summary
  const lang = localStorage.getItem('sv_lang') || 'en';
  // if user holds or ctrl? we'll just start recognition for command if supported
  if (recognition) {
    // ask user if they want command
    const want = confirm('Want to give a voice command? (e.g. "switch to Malayalam")\n\nCancel = play page audio.');
    if (want) {
      try { recognition.start(); } catch(e){ console.warn(e); speakCurrent(lang); }
      return;
    }
  }
  speakCurrent(lang);
});

